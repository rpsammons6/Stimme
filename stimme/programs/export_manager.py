    def _markdown_to_plain_text(self, markdown_text: str) -> str:
        """Convert markdown to plain text (basic conversion)"""
        # Remove markdown formatting
        text = markdown_text
        # Remove headers
        text = text.replace('#', '')
        # Remove bold/italic
        text = text.replace('**', '').replace('*', '')
        # Remove links (basic)
        import re
        text = re.sub(r'\[([^\]]+)\]\([^\)]+\)', r'\1', text)
        return text.strip()
    
