"""
Export Manager for Stimme - handles exporting translations to various formats
"""

import os
import json
import markdown
from datetime import datetime
from pathlib import Path
from typing import List, Dict, Any, Optional

class ExportManager:
    """Manages exporting translations to various formats"""
    
    def __init__(self, export_dir: str = None):
        self.export_dir = export_dir or os.path.join(os.getcwd(), "exports")
        self.ensure_export_dir()
    
    def ensure_export_dir(self):
        """Ensure export directory exists"""
        os.makedirs(self.export_dir, exist_ok=True)
    
    def set_export_directory(self, directory: str):
        """Set the export directory"""
        self.export_dir = directory
        self.ensure_export_dir()
    
    def get_export_directory(self) -> str:
        """Get the current export directory"""
        return self.export_dir
    
    def export_translation_txt(
        self, 
        source_text: str, 
        translation: str, 
        commentary: str = None,
        metadata: Dict[str, Any] = None,
        filename: str = None
    ) -> str:
        """
        Export translation to plain text format
        
        Returns:
            Path to the exported file
        """
        if not filename:
            timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
            # Create safe filename from first few words of source
            safe_source = "".join(c for c in source_text[:30] if c.isalnum() or c in (' ', '-', '_')).strip()
            safe_source = safe_source.replace(' ', '_')
            filename = f"translation_{safe_source}_{timestamp}.txt"
        
        filepath = os.path.join(self.export_dir, filename)
        
        with open(filepath, 'w', encoding='utf-8') as f:
            # Header
            f.write("=" * 60 + "\n")
            f.write("STIMME TRANSLATION EXPORT\n")
            f.write("=" * 60 + "\n\n")
            
            # Metadata
            if metadata:
                f.write("METADATA:\n")
                f.write("-" * 20 + "\n")
                f.write(f"Export Date: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n")
                f.write(f"Model: {metadata.get('model_used', 'Unknown')}\n")
                if metadata.get('translation_time'):
                    f.write(f"Translation Time: {metadata['translation_time']:.2f}s\n")
                f.write("\n")
            
            # Source text
            f.write("GERMAN SOURCE:\n")
            f.write("-" * 20 + "\n")
            f.write(source_text.strip() + "\n\n")
            
            # Translation
            f.write("ENGLISH TRANSLATION:\n")
            f.write("-" * 20 + "\n")
            f.write(translation.strip() + "\n\n")
            
            # Commentary if available
            if commentary:
                f.write("PHILOLOGICAL COMMENTARY:\n")
                f.write("-" * 20 + "\n")
                # Convert markdown to plain text (basic conversion)
                plain_commentary = self._markdown_to_plain_text(commentary)
                f.write(plain_commentary.strip() + "\n\n")
            
            # Footer
            f.write("=" * 60 + "\n")
            f.write("Generated by Stimme - Scholarly German Translation Tool\n")
            f.write("=" * 60 + "\n")
        
        return filepath
    
    def export_translation_html(
        self, 
        source_text: str, 
        translation: str, 
        commentary: str = None,
        metadata: Dict[str, Any] = None,
        filename: str = None
    ) -> str:
        """
        Export translation to HTML format with styling
        
        Returns:
            Path to the exported file
        """
        if not filename:
            timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
            safe_source = "".join(c for c in source_text[:30] if c.isalnum() or c in (' ', '-', '_')).strip()
            safe_source = safe_source.replace(' ', '_')
            filename = f"translation_{safe_source}_{timestamp}.html"
        
        filepath = os.path.join(self.export_dir, filename)
        
        # Use the existing formatter
        try:
            from formatter import get_styled_html
            html_content = get_styled_html(translation, commentary)
        except ImportError:
            # Fallback HTML generation
            html_content = self._generate_html(source_text, translation, commentary, metadata)
        
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(html_content)
        
        return filepath
    
