    def _format_timestamp(self, ts) -> Optional[str]:
        """Safely format a timestamp that could be a datetime, ISO string, or None."""
        if ts is None:
            return None
        if isinstance(ts, datetime):
            return ts.strftime('%Y-%m-%d %H:%M:%S')
        if isinstance(ts, str):
            try:
                return datetime.fromisoformat(ts).strftime('%Y-%m-%d %H:%M:%S')
            except (ValueError, TypeError):
                return ts  # Return as-is if it can't be parsed
        return str(ts)

    def _markdown_to_plain_text(self, markdown_text: str) -> str:
        """Convert markdown to plain text (basic conversion)"""
        # Remove markdown formatting
        text = markdown_text
        # Remove headers
        text = text.replace('#', '')
        # Remove bold/italic
        text = text.replace('**', '').replace('*', '')
        # Remove links (basic)
        import re
        text = re.sub(r'\[([^\]]+)\]\([^\)]+\)', r'\1', text)
        return text.strip()
    
    def get_supported_formats(self) -> List[str]:
        """Get list of supported export formats"""
        return ['txt', 'html', 'markdown']