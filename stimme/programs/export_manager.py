    def export_multiple_translations(
        self, 
        translations: List[Dict[str, Any]], 
        format_type: str = "txt",
        filename: str = None
    ) -> str:
        """
        Export multiple translations to a single file
        
        Args:
            translations: List of translation dictionaries
            format_type: Export format ('txt', 'html', 'markdown')
            filename: Optional custom filename
            
        Returns:
            Path to the exported file
        """
        if not filename:
            timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
            ext = "md" if format_type == "markdown" else format_type
            filename = f"stimme_translations_{timestamp}.{ext}"
        
        filepath = os.path.join(self.export_dir, filename)
        
        if format_type == "txt":
            self._export_multiple_txt(translations, filepath)
        elif format_type == "html":
            self._export_multiple_html(translations, filepath)
        elif format_type == "markdown":
            self._export_multiple_markdown(translations, filepath)
        else:
            raise ValueError(f"Unsupported format: {format_type}")
        
        return filepath
    
    def _export_multiple_txt(self, translations: List[Dict[str, Any]], filepath: str):
        """Export multiple translations to TXT format"""
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write("=" * 80 + "\n")
            f.write("STIMME TRANSLATION COLLECTION\n")
            f.write("=" * 80 + "\n")
            f.write(f"Exported: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n")
            f.write(f"Total Translations: {len(translations)}\n")
            f.write("=" * 80 + "\n\n")
            
            for i, trans in enumerate(translations, 1):
                f.write(f"TRANSLATION #{i}\n")
                f.write("-" * 40 + "\n")
                
                # Metadata
                ts = self._format_timestamp(trans.get('timestamp'))
                if ts:
                    f.write(f"Date: {ts}\n")
                if trans.get('metrics', {}).get('model_used'):
                    f.write(f"Model: {trans['metrics']['model_used']}\n")
                f.write("\n")
                
                # Source
                f.write("German Source:\n")
                f.write(f"{trans.get('source_full', trans.get('source', ''))}\n\n")
                
                # Translation
                f.write("English Translation:\n")
                f.write(f"{trans.get('translation', '')}\n\n")
                
                # Commentary
                if trans.get('commentary'):
                    f.write("Commentary:\n")
                    plain_commentary = self._markdown_to_plain_text(trans['commentary'])
                    f.write(f"{plain_commentary}\n\n")
                
                f.write("-" * 40 + "\n\n")
            
            f.write("=" * 80 + "\n")
            f.write("End of Collection\n")
            f.write("=" * 80 + "\n")
    
    def _export_multiple_markdown(self, translations: List[Dict[str, Any]], filepath: str):
        """Export multiple translations to Markdown format"""
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write("# Stimme Translation Collection\n\n")
            f.write(f"**Exported**: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}  \n")
            f.write(f"**Total Translations**: {len(translations)}\n\n")
            f.write("---\n\n")
            
            for i, trans in enumerate(translations, 1):
                f.write(f"## Translation #{i}\n\n")
                
                # Metadata
                ts = self._format_timestamp(trans.get('timestamp'))
                model = trans.get('metrics', {}).get('model_used')
                if ts or model:
                    if ts:
                        f.write(f"- **Date**: {ts}\n")
                    if model:
                        f.write(f"- **Model**: {model}\n")
                    f.write("\n")
                
                # Source
                f.write("### German Source\n\n")
                source = trans.get('source_full', trans.get('source', ''))
                f.write(f"> {source}\n\n")
                
                # Translation
                f.write("### English Translation\n\n")
                f.write(f"{trans.get('translation', '')}\n\n")
                
                # Commentary
                if trans.get('commentary'):
                    f.write("### Commentary\n\n")
                    f.write(f"{trans['commentary']}\n\n")
                
                f.write("---\n\n")
            
            f.write("*Generated by Stimme - Scholarly German Translation Tool*\n")
    
    def _export_multiple_html(self, translations: List[Dict[str, Any]], filepath: str):
        """Export multiple translations to HTML format"""
        html_content = self._generate_collection_html(translations)
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(html_content)
    
